//demonstrate the use of switch case statement
let fruit = "kiwi";
switch(fruit){
        case "apple":
        console.log("apple is 100/kg")
        break //use for breaking and stop the code to execute fully 
        case "banana":
        console.log("banana is 40/12")
        break
        case "orange":
        console.log("orange is 10/kg")
        break
        case "kiwi":
        console.log("kiwi is 20/piece")
        break
        case "chiku":
        console.log("chiku is 70/kg")
        break
        default:
            console.log("out of stock")
}